# ╔═╡ 04d20e5b-492d-4eff-93a2-5feb693ad565
md"""
# *ADNS*: Automatic differentiation nested sampling

First and quick stab at sampling directly from a maximum entropy (ME) distribution for networks, where networks are sampled dynamically under a given soft constraints of the number of edges and the network global clustering coefficient. It works quite well **without any tuning** up to `N = 10` nodes (such that `ndim = 100`) (haven't tested beyond -- who knows?).

This is yields a generic sampling algorithm for any [exponential family random graph model (ERGM)](https://en.wikipedia.org/wiki/Exponential_family_random_graph_models) but probably limited to a couple of dozen nodes.
(Though if only one constraint is required and the other ones are implemented in the prior, the nested sampling run only needs to be done once, freeing up time for increasing the number of dimensions.)

The gradient $\nabla_\lambda \log Z$ is quite sensitive to the value of $λ$ and $\lambda \sigma \sim N(0,1)$ where $\sigma$ is the stdev of the statistic for $\lambda$ under the prior. (You can show this by expanding the ME distribution in $\lambda$).

!!! note "Abstract"
    ADNS is about:
	- Differentiating the log partition function $\log Z$ directly using *automatic differentiation*. This allows one to solve for the Lagrange multipliers in maximum entropy (or miniumum Kullback-Leibler distance) problems and **evaluate** all kinds of statistics directly.
	- And, in the same line of reasoning, dynamically **sample** from the new maximum entropy (or miniumum Kullback-Leibler distance) distribution by casting it as a posterior in a standard nested sampling problem.
"""

# ╔═╡ 7f086dac-7d20-4f30-87bf-132eb2453c50
md"## Setup constraints and prior"

# ╔═╡ 42170bbe-6e56-4199-bf20-378b6fa681d4
"""
Implementation of <https://graph-tool.skewed.de/static/doc/clustering.html#graph_tool.clustering.global_clustering>.

Note that this implementation is a bit different than others I have seen, but it doesn't matter, since it is only an example statistic which I use to illustrate that a highly nonlinear function can be used. BTW I don't think that the global clustering coefficient is very useful in practice LOL
"""
function global_clustering_coefficient(A)
	Aₙ = normalize(A, 1)
	Aₙ² = Aₙ * Aₙ
	Aₙ³ = Aₙ² * Aₙ

	numerator = tr(Aₙ³)
	denominator = let s = 0.
		for j in 1:size(Aₙ², 2)
			for i in 1:size(Aₙ², 1)
				if i != j
					s += Aₙ²[i, j]
				end
			end
		end
		s
	end
	c = numerator/denominator
	return c
end

# ╔═╡ 0faf5355-3971-491c-b3f9-573f22d6bcd1
begin
	# Number of nodes
	N = 5
	ndim = N^2

	bounds = Bounds.MultiEllipsoid
	#prop = Proposals.RSlice(slices=10, scale=1/ndim)
	nlive = 2*ndim # `2ndim` is the very minimum

	f₁(A) = sum(A)
	f₂(A) = global_clustering_coefficient(A)

	prior = Poisson(N)

	function ptform(U)
		return Float64.(quantile(prior, U))
	end

	function adjacency(X)
		A = reshape(X, (N, N))
		return A
	end

	function ns(λ)
		function logl(X)
			A = adjacency(X)
			return -λ[1]*f₁(A) - λ[2]*f₂(A)
		end
		
		model = NestedModel(logl, ptform)
		sampler = Nested(ndim, nlive; bounds=bounds)

		chain, state = sample(model, sampler; dlogz=0.1)
		return chain, state
	end

	function logz(λ)
		chain, state = ns(λ)
		return state.logz
	end

	"""**ADNS** -- Differentiate through an entire nested sampling run ( ͡° ͜ʖ ͡°)"""
	dlogz(λ) = ForwardDiff.gradient(logz, λ)
end

# ╔═╡ 35aae348-f00e-40e4-9998-7b8ccfe81be7
σ₁, σ₂ = let
	A = adjacency.(ptform.([rand(ndim) for _ in 1:1000]))
	std(f₁.(A)), std(f₂.(A))
end

# ╔═╡ b61774ad-36e5-49e9-aed3-fa980721da63
lambdaprior() = [randn()/σ₁, randn()/σ₂]

# ╔═╡ ba44aa69-dc40-47ed-a39b-2d7d4eb6924f
md"""
## Sample from the posterior

These samples from the chain are effectively from the posterior distribution that is as close as possible (i.e. minimum Kullback-Leibler distance) to the original prior (`ptform()` above) while agreeing with the new soft constraint (i.e., the global clustering coefficient must average to a given number which depends on $\lambda$).
"""

# ╔═╡ a74bf883-0b80-4dcb-a10e-5f4aec6f669c
begin
	λ = lambdaprior()
	chain, state = ns(λ)
	chain, state
end

# ╔═╡ 0fc4c554-29ef-421f-9050-2dda72c452a8
@info "Expected value of M for λ₁ = 0:" N*ndim

# ╔═╡ 6b98c787-797e-4834-84a1-865b54db9e5f
md"""
The gradient of $\log Z$ is not so stable for large values of $\lambda$. This is entirely expected because we are setting extreme constraints -- our prior is not rich enough to accomodate them easily. This might be ameliorated by extra tuning to the NS exploration algorithm, but the ground issue is that the prior is not expressive enough for the constraint we are setting.
"""

# ╔═╡ 6a055ddd-58b0-44c7-bc4e-5c30d07f5519
λ, -dlogz(λ)

# ╔═╡ 69b787a8-6bd4-40ba-8a0e-bf8700e961f1
md"""
When $\lambda_1 = 0$, there is no effect of $f_1(A)$ on the maximum entropy distribution $p(A|\lambda)$, so the expected number of edges $<M>_{p(A|\lambda)} = <M>_{q(A)}$ which is `N` (the argument to `Poisson(N)`) times `ndim`.
"""

